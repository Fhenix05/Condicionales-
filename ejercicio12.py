dado = int(input("Ingresa el resultado del dado: "))


if dado >= 1 and dado <= 6:
    if dado == 1:
        print("Seis")
    elif dado == 2:
        print("Cinco")
    elif dado == 3:
        print("Cuatro")
    elif dado == 4:
        print("Tres")
    elif dado == 5:
        print("Dos")
    elif dado == 6:
        print("Uno")
else:
    print ("Error")